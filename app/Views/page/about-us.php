<h1>About Us</h1>

<p>Indonesia Book Vendor was established in 2006 as a book vendor specializing in journals, monographs, periodicals, and all other publications published in Indonesian and indigenous languages of Indonesia. IBV specifically addresses the need of University Libraries in the United States, Asia Pacific, and Europe. In general, we also cater to individual, governmental, private, and other institutional needs for books about Indonesia in various disciplines.</p>

<h3>Our Products</h3>

<p>Recently published books from Indonesia cover a wide range of topics including history, culture, social, politics, economics, human rights issues, law, travel industry, hobbies, novel, literature, textbooks, newspapers, magazines, bulletins, journals, and many more. Aside from books, IBV also offers DVD, VCD, VHS, CD, and CR-Rom about, related to, or made in Indonesia(n).</p>

